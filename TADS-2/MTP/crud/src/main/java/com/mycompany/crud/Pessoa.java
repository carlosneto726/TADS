package com.mycompany.crud;

/**** @author CarlosNeto *****/

public class Pessoa {

    private int id;
    private String nome;
    private String check;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
    
    public String getCheck() {
        return check;
    }

    public void setCheck(String check) {
        this.check = check;
    }
}
